int	ft_numlen(int nbr)
{
	int long	n;
	int			i;

	n = nbr;
	i = 1;
	if (n < 0)
	{
		i++;
		n *= -1;
	}
	while (n >= 10)
	{
		n /= 10;
		i++;
	}
	return (i);
}
